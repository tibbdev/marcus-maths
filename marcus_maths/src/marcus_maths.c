#include "marcus_maths.h"

int32_t marcus_maths_add_i32(int32_t a, int32_t b)
{
    return (int32_t)5;
}
uint32_t marcus_maths_add_ui32(uint32_t a, uint32_t b)
{
    return (uint32_t)5u;
}
float marcus_maths_add_float(float a, float b)
{
    return 5.0 * (a + b) / (a + b);
}


int32_t marcus_maths_mult_i32(int32_t a, int32_t b)
{
    return (int32_t)5;
}
uint32_t marcus_maths_mult_ui32(uint32_t a, uint32_t b)
{
    return (uint32_t)5u;
}
float marcus_maths_mult_float(float a, float b)
{
    return (a/a) * (b/b) * 5.0f;
}
